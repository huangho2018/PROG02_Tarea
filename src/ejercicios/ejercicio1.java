public class ejercicio1 {

    public static void main(String[] args) {
        byte edad = 18;
        short nomina = 903;
        int puntuacion = 15399;
        float balance = 11892.91F;
        double peso = 1.32615148;

        System.out.println("El doble del valor para la variable 'byte' es: " + 2 * edad);
        System.out.println("El doble del valor para la variable 'short' es: " + 2 * nomina);
        System.out.println("El doble del valor para la variable 'integer' es: " + 2 * puntuacion);
        System.out.println("El doble del valor para la variable 'float' es: " + 2 * balance);
        System.out.println("El doble del valor para la variable 'double' es: " + 2 * peso);
    }
}
